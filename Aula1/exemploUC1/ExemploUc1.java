/*
    Tipos Básicos de Variáveis:
        - int : Números inteiros de -2.147.483.648 a 2.147.483.647
        - byte: Números inteiros de -128 a 127
        - short : Números inteiros de -32.768 a 32.767
        - long : Números inteiros de -9.223.372.036.854.775.808 a 9.223.372.036.854.775.807
        - float : Números reais de 6 a 7 dígitos decimais
        - double : Números reais de 15 dígitos decimais
        - char : Variavel que aceita a inserção de apenas um único caratere.
        - String : Variável que aceita um Texto (mais de um caractere)
        - boolean : True ou False

*/


//Programa Exemplo: para criação de variável numérica do tipo inteiro (idade), uma String (nome) e mostrar no terminal (print)
public class ExemploUc1 {
    
public static void main(String[] args) {
    int idade = 51; //Vriaçãio de variável int
    String nome = "Gerson Risso"; //Criação de variável String

    //Mostrando no terminal
    System.out.println(nome);
    System.out.println(idade);
}
}
