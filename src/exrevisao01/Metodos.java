
package exrevisao01;

/*
elabore um projeto java que calcule a area de um quadrado e de um retangulo
*/

public class Metodos {
    
    /**
     * calcula a area do quadrado
     * @param lado double
     * @return 
     */
    public static double area(double lado){
        return lado * lado;
    }
    
    /**
     * retorna area do retangulo
     * @param lado double
     * @param altura double
     * @return 
     */
    public static double area(double lado, double altura){
        return lado * altura;
    }
}
